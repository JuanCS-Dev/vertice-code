import logging
from typing import Any, AsyncIterator, Dict

from .base import BaseGenerator

logger = logging.getLogger(__name__)


class TerraformGenerator(BaseGenerator):
    """Generate Terraform IaC configurations."""

    @property
    def name(self) -> str:
        return "terraform"

    async def generate(self, task_request: str) -> Dict[str, Any]:
        """Generate Terraform configuration."""
        logger.info("Generating Terraform configuration...")

        terraform = self._build_main_tf()

        return {
            "main.tf": terraform,
            "features": [
                "EKS cluster with managed node groups",
                "VPC with public/private subnets",
                "Remote state in S3 with locking",
                "Production-ready configuration",
            ],
        }

